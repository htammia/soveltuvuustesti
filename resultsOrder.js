function getOrder() {
  let order = [];
  const query = window.location.search;
  const urlParams = new URLSearchParams(query);
  order[0] = urlParams.get("st");
  order[1] = urlParams.get("nd");
  order[2] = urlParams.get("rd");

  return order;
}